package main

import "fmt"

func main() {
	var a int = 23
	var b int = 7

	fmt.Println("a + b =", a + b)
	fmt.Println("a - b =", a - b)
	fmt.Println("a * b =", a * b)
	fmt.Println("a / b =", a / b)

	var c float64 = 23
	var d float64 = 7

	fmt.Println("c / d =", c / d)
	fmt.Println("a % b =", a % b)
}